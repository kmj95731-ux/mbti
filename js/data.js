// ── localStorage 유틸 ──
function lsGet(k, d) { try { const v = localStorage.getItem(k); return v !== null ? JSON.parse(v) : d; } catch { return d; } }
function lsSet(k, v) { try { localStorage.setItem(k, JSON.stringify(v)); } catch {} }

function getUserKey() {
  let k = localStorage.getItem('_uid');
  if (!k) { k = 'u' + Math.random().toString(36).slice(2) + Date.now().toString(36); localStorage.setItem('_uid', k); }
  return k;
}
